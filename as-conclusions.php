                            <h3>Conclusion: Urban Opportunities</h3>
                            <h5>Brands and corporations are waking up to the opportunities inherent in the emergence of cities as ‘the news nations’</h5>

                        <p>The sheer population density of cities obviously has its attractions when thinking about marketing and communications in terms of ‘cost per thousand’. </p>

                        <p>But cityscapes themselves represent opportunities for brands to show their playful side. In our <a href="https://www.grayling.com/global/en/insight/six_things_every_professional_communicator_needs_to_know_for_2018">#6into18</a> trends forecast, we identified the increasing use of cities as playgrounds, whether that’s interactive installations, pop-ups or <a href="https://www.youtube.com/watch?v=9rj8mYSuR-Y">projections</a> – all totally Instagramable, of course. 
                            </p>

                            <p>The rapid growth of cities does not come without its down-sides, though. In cities all over the world, divisions between the haves and have-nots are stark. The UN <a href="https://www.theguardian.com/us-news/2018/jan/22/un-rapporteur-homeless-san-francisco-california">found</a> levels of homelessness, poverty and squalor on par with the slums of Mumbai and favelas of Brazil… in San Francisco, one of the richest cities on earth. Non-profits like <a href="https://lavamae.org">Lava Mae</a> are doing their bit, and brands for whom social purpose is a core part of their make-up are also <a href="https://www.grayling.com/global/en/insight/take_a_stand">taking a stand</a>: In Athens, for example, LG is <a href="https://www.bbc.com/news/av/stories-44196085/the-mobile-laundry-for-homeless-people">equipping</a> vans with washing machines for the city’s homeless population. And in Africa, where wildlife populations are at risk of being wiped out as a result of deforestation to make way for urban development, brands and corporations have the opportunity to take action, in a way that will not only burnish their environmental credentials, but can also effect change.</p>

                            <p>There are also challenges and risks to a city-first approach to marketing communications: While more than half of the world’s population lives in cities, what about the other 45%? As we have noted, there are significant divisions between urban and rural communities on a range of issues, and overt city-first strategies risk alienating non-city dwellers. </p>

                            <p>Successful marketers must recognise the power inherent in ‘the new nations’, and understand the care and attention required to leverage the different spheres of influences.</p>
                        
                            
