                                    <h3>The Media</h3>
                                    <h5>While the mediascape continues to undergo its long and sometimes painful digital transformation, media brands old and new continue to hold considerable sway, at both a national and local level. Influencing the media has always been a central tenet of the public relations industry, but the micro-targeting now possible with digital media requires a greater level of sophistication than the ‘spray and pray’ merchants of old.</h5>
            
                                    <p><b class="description__header">Why mainstream media still matter.</b></p>
                                    <p>For the longest time, newspapers and other mass media were the primary means of influencing opinions and behavior. This was the mainstay of the public relations profession. But over the past 20 years, that landscape has changed dramatically. And it continues to evolve.</p>
            
                                    <p>Print media, once the dominant source of news and information, has been in a long slow decline, by almost every metric. Circulations of daily newspapers’ print editions are falling at a rate of around 10% a year; newsrooms have shed tens of thousands of journalism jobs, and the number of actual newspapers is in decline, too: Local news is the main victim of this, as running local newsrooms or employing local reporters becomes less economically viable. And attempts to make print millennial-friendly have fallen flat. </p>
            
                                    <p>But while print may be dead (or at least dying), newspapers and magazines are brands. And strong brands are very hard to kill. </p>
            
                                    <p>Media brands have, over many years and with varying degrees of success, been transitioning to a digital-first model. Major media brands like The New York Times and Financial Times continue to see quarter on quarter increases in digital paid subscriptions, which off-set the declines not just in print sales, but in ad revenue. Deloitte estimates that while in 2012 the ad to subscription revenue ration was 90:10, by 2020 it is more likely to be 50:50. </p>
            
                                    <p>Ironically, it is online where the true influence of these media brands is really felt, today. Because of their sites’ high domain authority, their content appears prominently in most searches, increasing their reach exponentially. You may not buy a daily newspaper anymore, but thanks to search, the potential reach of media brands is greater than ever. </p>
            
                                    <p>But what does all this mean for cities? </p>
            
                                    <p>The key issue is one of relevance. News media have always been accused of having a metropolitan bias, and if that is true then it is because publishers have always had a relentless focus on understanding their audiences, so that they can meet their needs, give the people what they want. The simple fact is that cities are where their audiences are, and where the economic and cultural influence resides. The growth in the number and size of cities, alongside the death of local news is only going to exacerbate this.</p>
            
                                    <p>The simple fact is that ‘national’ media really talk to people in cities.</p>
                                   
