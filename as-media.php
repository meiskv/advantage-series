                                            <p><b class="description__header">A new breed of media</b></p>
                                            <p>Alongside the evolution and digital transformation of established media brands, we are witnessing the emergence of a new breed of digital-first media, aimed squarely at the particular interests and usage patterns of younger urbanites.</p>
                                            <p>Platforms like BuzzFeed have evolved from publishers of quirky content, to media giants in their own right, breaking hard news stories and disrupting the media landscape by delivering engaging content in a way that is geared towards the video-snacking habits of digital natives on the move.</p>
                                            <p>And while the financial viability of the likes of BuzzFeed, Vox, Vice Media and Mashable has been called into question, as one after the other has missed its targets, their success with younger city dwellers is hard to argue with. This is in part due to the fact that unlike ‘The Gray Lady’, these media were founded in the digital age, and in the social media age. They recognized early on the habits of their target audience, and the importance of social media. An estimated 80% of BuzzFeed’s reach exists beyond its website, on its apps, on social media platforms, messaging apps and video syndication partners. This gives them not just incredible reach, but engagement – consumers of these media do not have to visit one site to access the content they want; that content is served up to them, on whatever channels they happen to prefer.</p>
