                            <h3>Conclusion: Urban Opportunities</h3>
                            <h5>Brands and corporations are waking up to the opportunities inherent in the emergence of cities as ‘the news nations’</h5>

                        <p>The sheer population density of cities obviously has its attractions when thinking about marketing and communications in terms of ‘cost per thousand’. </p>

                        <p>But cityscapes themselves represent opportunities for brands to show their playful side. In our #6into18 trends forecast, we identified the increasing use of cities as playgrounds, whether that’s interactive installations, pop-ups or projections – all totally Instagramable, of course. 
                            </p>

                            <p>The rapid growth of cities does not come without its down-sides, though. In cities all over the world, divisions between the haves and have-nots are stark. The UN found levels of homelessness, poverty and squalor on par with the slums of Mumbai and favelas of Brazil… in San Francisco, one of the richest cities on earth. Non-profits like Lava Mae are doing their bit, and brands for whom social purpose is a core part of their make-up are also taking a stand: In Athens, for example, LG is equipping vans with washing machines for the city’s homeless population. And in Africa, where wildlife populations are at risk of being wiped out as a result of deforestation to make way for urban development, brands and corporations have the opportunity to take action, in a way that will not only burnish their environmental credentials, but can also effect change.</p>

                            <p>There are also challenges and risks to a city-first approach to marketing communications: While more than half of the world’s population lives in cities, what about the other 45%? As we have noted, there are significant divisions between urban and rural communities on a range of issues, and overt city-first strategies risk alienating non-city dwellers. </p>

                            <p>Successful marketers must recognize the power inherent in ‘the new nations’, and understand the care and attention required to leverage the different spheres of influences.</p>
                        
                            
