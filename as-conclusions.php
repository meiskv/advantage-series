                            <p>The rapid growth of cities does not come without its down-sides, though. In cities all over the world, divisions between the haves and have-nots are stark. The UN found levels of homelessness, poverty and squalor on a par with the slums of Mumbai and favelas of Brazil… in San Francisco, one of the richest cities on earth. Non-profits like Lava Mae are doing their bit, and brands for whom social purpose is a core part of their make-up are also taking a stand: In Athens, for example, LG is equipping vans with washing machines for the city’s homeless population. And in Africa, where wildlife populations are at risk of being wiped out as a result of deforestation to make way for urban development, brands and corporations have the opportunity to take action, in a way that will not only burnish their environmental credentials, but can also effect change.</p>

                            <p>But there also challenges and risks to a city-first approach to marketing communications: While more than half of the world’s population lives in cities, what about the other 45%? As we have noted, there are significant divisions between urban and rural communities on a range of issues, and overt city-first strategies risk alienating non-city dwellers. </p>

                            <p>Successful marketers must recognize the power inherent in ‘the new nations’, and understand the care and attention required to leverage the different spheres of influences.</p>
                        
                            
