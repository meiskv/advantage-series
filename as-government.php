                        <h3>Government</h3>
                        <h5>Major cities have huge economic clout, and with that comes political influence. Across the world, city mayors have considerable strategic and executive powers, meaning decisions about everything from the public realm to inward investment policies can lie in the hands of one woman or man and a small group of advisors. How can those decisions be influenced and leveraged is a fundamental question for many.</h5>

                        <p><b class="description__header">Decentralization</b></p>
                        <p>Achieving the optimum balance of power between national and local government is an issue with which countries around the world have wrestled for decades. It is not limited to big countries, or large economies, and no country is a completely unified state – every state is at least composed of municipalities as decentralized units. The degree to which power is decentralized or devolved varies dramatically between nations (as do the motivations), but the consensus is that since the 1980s trend the world over has been towards increased decentralization. According to the OECD, as of 2016 there were almost 138,000 sub-national governments in the OECD’s 35 member countries alone.  And there can be little doubt that this trend has at least in part been driven by the increasing economic power of cities.</p>

                        <p>But this decentralization creates an inevitable tension between national governments which, generally speaking, want to maximize political and fiscal control, and cities, which have a strong sense of identity and recognize the leverage they have as a result of their economic clout. </p>

                        <p>This tension manifests itself in many ways, but no more so than when cities are at odds with the policies of national government.</p>

                        <p>Boston University’s Professor Graham Wilson, director of the Initiative on Cities, has noted that in the US, tension between the federal government and cities has existed for some time, but has become more evident under President Trump. We now see cities and states increasingly taking on issues related to immigration, climate change and policing — all policy areas traditionally undertaken by the federal government.</p>

                        <p>In the US, so-called ‘sanctuary cities’, transgender bathrooms, fracking and other issues have become lightening rods for the tension that exists between cities and federal (and/or state) government.</p>

                        <p>But this tension is more complex than ‘us versus them’; city and federal governments often need to collaborate to solve complex challenges. Germany’s response to the continuing refugee crisis is a case in point.</p>

                        <p>Germany has received more than twice as many asylum applications as any other European country, and cities have borne the brunt of this, having to find ways to accommodate the influx while dealing with existing pressures on housing and public services. Germany’s framework for allocating funding and expenditures across federal, state, and city governments imposes uneven burdens on city-states (Berlin, Bremen, Hamburg) and large cities; uniform reimbursement rates from the federal government fail to take account of variations in housing costs, cost of living, and per-capita social service expenditures. But the German cities and city-states have stepped up and found innovative ways of responding to the issue. Their experiences are giving them a seat at the national policy-making table, on this and potentially other related issues.</p>

                        <br/>

                        <p><b class="description__header">Cities as international actors</b></p>
                        <p>The urban / rural divisions have always existed, but have become more pronounced (or at least more evident) in recent years, as the more socially liberal city dwellers behave, and vote one way, and those outside the major conurbations have seemingly diametrically opposed views. Consider the voting results from the 2016 US Presidential election, the result of the UK’s Brexit referendum, and subsequent election results in France, the Czech Republic and Hungary. Rural voters, for now at least, are getting their way.</p>

                        <p>So it is perhaps not surprising that when it comes to policy positions, many cities are flexing their muscles, reaching beyond their national boundaries, and the positions of their national leaders, to claim a place on the world stage.</p>

                        <p>The most high profile example is the backlash against the US government withdrawing from the Paris Climate Accord, under President Trump. In the wake of this announcement, a coalition of US cities, states, companies and universities pledged to continue meeting the commitments of the Paris Accord. In the year following Trump’s announcement, more than 2,700 leaders from states, cities, and businesses – representing 159 million people and $6.2 trillion in GDP – signed up to America’s Pledge.</p>
                        <br/>
                        <p><b class="description__header">A tweet at the table</b></p>
                        <p>These cities are, of course, reflecting the views of their populations, which tend to be younger, more diverse, and more progressive on social issues. Technology is giving governments at every level the opportunity to be more responsive. </p>

                        <p>As noted in Grayling’s ‘Me, Myself and AI’ report, many local governments are experimenting with new technologies that make them both more accessible and more accountable. While cybersecurity remains the biggest expenditure, we have also seen the introduction of chatbots, greater use of the cloud, and even some forays into artificial intelligence, all intended to improve the running of cities. Meanwhile, connected technologies being applied are turning cities into smart cities, making them safer, more energy efficient, and better connected. </p>

                        <p>But beyond the actual running of cities, technology can also bring people and their elected representatives closer together. Most politicians are savvy enough to recognize the benefits of having a dialogue with their constituents through social media platforms, and online petitions give people the opportunity to bring to the attention of city councils, issues that might not otherwise receive air time.</p>
                                
